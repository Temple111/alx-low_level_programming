#include <stdio.h>

/**
 * main - this program checks if the last digit of anumber is >, < or zero
 * Return: 0 (sucessful)
 */
int main(void)
{
	char b, c;

	b = 'a';
	c = 'A';
	while
		(b <= 'z') {
			putchar(b);
			b++;
		}
	while
		(c <= 'Z') {
			putchar(c);
			c++;
		}
	putchar('\n');
	return (0);
}
