#include <stdio.h>

/**
 * main - this program checks if the last digit of anumber is >, < or zero
 * Return: 0 (sucessful)
 */
int main(void)
{
	char c;

	int d;

	c = 'a';
	d = 0;
	while
		(d < 10) {
			putchar(d + '0');
			d++;
		}
	while
		(c <= 'f') {
			putchar(c);
			c++;
		}
	putchar('\n');
	return (0);
}
